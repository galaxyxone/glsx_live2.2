class Wallet extends Component {
  state = {
    message: "",
  };

  // Constructor
  constructor(props) {
    super(props);

    this.state = {
      items: [],
      DataisLoaded: false,
      profile: {},
      balance: 0,
    };
  }

  // ComponentDidMount is used to
  // execute the code
  componentDidMount() {
    this.loadUserProfile();
    fetch("https://jsonplaceholder.typicode.com/users")
      .then((res) => res.json())
      .then((json) => {
        this.setState({
          items: json,
          DataisLoaded: true,
        });
      });
  }

  loadUserProfile() {
    this.props.auth.getProfile((profile, error) => {
      this.setState({ profile, error });
      this.getBalance();
    });
  }

  async getBalance() {
    const { DataisLoaded, profile } = this.state;
    console.log({ profile });
    if (DataisLoaded && profile.app_metadata) {
      const web3 = new Web3("https://bsc-dataseed1.binance.org:443");
      let contractInstance = new web3.eth.Contract(
        contractABI,
        profile.app_metadata.contractaddr
      );
      let balance = await contractInstance.methods.balanceOf(
        profile.app_metadata.walletaddr
      );
      console.log(balance);
      balance = balance / 1e18;
      this.setState({
        balance: balance,
      });
    }
  }

  render() {
    const { DataisLoaded, profile } = this.state;
    if (!DataisLoaded)
      return (
        <div>
          <h1> Please wait some time.... </h1>{" "}
        </div>
      );

    return (
      <div className="Wallet">
        <h1> My Certificates of Authenticity </h1>
        {profile &&
          profile.app_metadata &&
          profile.user_metadata.art.map((data) => (
            <>
              <h3>{data.userart}</h3>
              <a href={data.walletaddr} target="_blank">
                {"Wallet Addr"}
              </a>
            </>
          ))}
        {/* <h3>
          {(profile && profile.app_metadata && profile.user_metadata.userart) ||
            ""}
        </h3>
        <h3>
          {(profile &&
            profile.app_metadata &&
            profile.user_metadata.walletaddr) ||
            ""}
        </h3>
        <h3>
          {(profile &&
            profile.app_metadata &&
            profile.user_metadata.userart1) ||
            ""}
        </h3>
        <h3>
          {(profile &&
            profile.app_metadata &&
            profile.user_metadata.walletaddr1) ||
            ""}
        </h3> */}
      </div>
    );
  }
}

export default Wallet;
